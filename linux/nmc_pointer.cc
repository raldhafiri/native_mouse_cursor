// X11 backend support for warpPointer.
//
// We do NOT link libX11 at build time. Doing so drags the system libX11 onto the
// final executable's link line, and on snap-packaged Flutter the old bundled
// toolchain (Clang 10 / old GLIBC) can't resolve the newer GLIBC symbols a
// modern system libX11 references (dlopen@GLIBC_2.34, __isoc23_strtol@2.38, …)
// → an undefined-reference link error in the host app.
//
// Instead we resolve XWarpPointer at RUNTIME via dlsym: when the app runs on an
// X11 session, GDK's X11 backend has already loaded libX11 into the process, so
// dlsym(RTLD_DEFAULT, "XWarpPointer") finds it with zero build-time linkage and
// no GLIBC coupling. GDK's gdk/gdkx.h still gives us GDK_IS_X11_DISPLAY /
// GDK_DISPLAY_XDISPLAY (header-only macros; their symbols are in the GTK lib we
// already link). On Wayland (or a build without the X11 GDK backend) the path is
// compiled/short-circuited out and warpPointer reports unsupported.
#ifdef GDK_WINDOWING_X11
#include <gdk/gdkx.h>
#define NMC_X11_RUNTIME 1
#endif

// Wayland infinite-drag support. Wayland forbids absolute pointer warping, but
// pointer-constraints-v1 (lock the pointer) + relative-pointer-v1 (keep getting
// relative deltas while locked) give the SAME effect as the web's Pointer Lock:
// an unbounded relative-motion stream with no edges. We expose it through the
// same lockPointer / unlockPointer / drainPointerLockDelta surface the web uses,
// so the Dart InfiniteDragController treats Wayland exactly like the web.
//
// Like the X11 path, we DON'T link libwayland-client (snap-toolchain GLIBC
// trap); we resolve the handful of wl_proxy_* core functions and the GDK-Wayland
// accessors at RUNTIME via dlsym (GDK already loaded both on a Wayland session).
// NMC_WAYLAND_HAVE_PROTOCOLS is defined by CMake when wayland-scanner produced
// the protocol glue (correct wl_interface tables) and we're on a Wayland-capable
// GTK. Only then do we compile the lock-pointer path.
#if defined(GDK_WINDOWING_WAYLAND) && defined(NMC_WAYLAND_HAVE_PROTOCOLS)
#include <gdk/gdkwayland.h>
#include <wayland-client-core.h>
#include <wayland-client-protocol.h>
// Generated by wayland-scanner from the system wayland-protocols XML (build dir
// on the include path). Provide the real interface descriptors + opcodes/enums.
#include "pointer-constraints-unstable-v1-client-protocol.h"
#include "relative-pointer-unstable-v1-client-protocol.h"
#define NMC_WAYLAND_RUNTIME 1
#endif

#include <cstdint>
#include <cstring>

// Per-plugin pointer state. Holds the borrowed registrar (to reach the
// GdkWindow) and, on Wayland, the bound managers + per-lock proxies + the
// accumulated relative motion.
struct _NmcPointer {
  FlPluginRegistrar* registrar;  // borrowed

  void* wl_constraints;       // zwp_pointer_constraints_v1* (bound ONCE, cached)
  void* wl_rel_manager;       // zwp_relative_pointer_manager_v1* (bound once)
  bool wl_bind_tried;         // we attempted to bind the managers already
  void* wl_locked_pointer;    // zwp_locked_pointer_v1* (per-lock)
  void* wl_relative_pointer;  // zwp_relative_pointer_v1* (per-lock)
  double wl_rel_dx;           // accumulated relative motion (logical px)
  double wl_rel_dy;
};

static double value_to_double(FlValue* v, double fallback) {
  if (v == nullptr) return fallback;
  switch (fl_value_get_type(v)) {
    case FL_VALUE_TYPE_INT:
      return static_cast<double>(fl_value_get_int(v));
    case FL_VALUE_TYPE_FLOAT:
      return fl_value_get_float(v);
    default:
      return fallback;
  }
}

static GdkWindow* get_gdk_window(NmcPointer* self) {
  if (self->registrar == nullptr) return nullptr;
  FlView* view = fl_plugin_registrar_get_view(self->registrar);
  if (view == nullptr) return nullptr;
  GtkWidget* toplevel = gtk_widget_get_toplevel(GTK_WIDGET(view));
  return gtk_widget_get_window(toplevel);
}

// ───────────────────────────────── X11 warp ─────────────────────────────────
#ifdef NMC_X11_RUNTIME
// XWarpPointer / XFlush resolved at runtime via dlsym (libX11 is already loaded
// by GDK's X11 backend on an X11 session). Signatures match Xlib's; we use
// XID/Display* from gdk/gdkx.h (which pulls in Xlib's types) so no extra link.
typedef int (*XWarpPointerFn)(Display*, ::Window, ::Window, int, int, unsigned,
                              unsigned, int, int);
typedef int (*XFlushFn)(Display*);

static XWarpPointerFn nmc_x_warp() {
  static XWarpPointerFn fn =
      reinterpret_cast<XWarpPointerFn>(dlsym(RTLD_DEFAULT, "XWarpPointer"));
  return fn;
}
static XFlushFn nmc_x_flush() {
  static XFlushFn fn =
      reinterpret_cast<XFlushFn>(dlsym(RTLD_DEFAULT, "XFlush"));
  return fn;
}
#endif

FlMethodResponse* nmc_pointer_can_warp(NmcPointer* self) {
  bool ok = false;
#ifdef NMC_X11_RUNTIME
  GdkDisplay* display = gdk_display_get_default();
  if (display != nullptr && GDK_IS_X11_DISPLAY(display) &&
      nmc_x_warp() != nullptr) {
    ok = true;
  }
#endif
  (void)self;
  return FL_METHOD_RESPONSE(
      fl_method_success_response_new(fl_value_new_bool(ok)));
}

FlMethodResponse* nmc_pointer_warp(NmcPointer* self, FlValue* args) {
#ifdef NMC_X11_RUNTIME
  GdkDisplay* display = gdk_display_get_default();
  XWarpPointerFn warp = nmc_x_warp();
  FlView* view = self->registrar
                     ? fl_plugin_registrar_get_view(self->registrar)
                     : nullptr;
  if (display != nullptr && GDK_IS_X11_DISPLAY(display) && warp != nullptr &&
      view != nullptr) {
    double lx = value_to_double(fl_value_lookup_string(args, "x"), 0);
    double ly = value_to_double(fl_value_lookup_string(args, "y"), 0);
    GdkWindow* window = gtk_widget_get_window(GTK_WIDGET(view));
    if (window != nullptr) {
      // x/y are Flutter-LOGICAL coords relative to the FlView (top-left,
      // y-down). Map to root (screen) X11 pixels. We anchor on the FlVIEW's own
      // window origin — NOT the toplevel's — because the toplevel origin can be
      // offset by window decorations / a header bar, which made the warp land
      // wrong vertically ("jump to top"). If the view shares the toplevel's
      // window (no native subwindow), add the view's allocation offset so (0,0)
      // still maps to the view's top-left.
      gint scale = gdk_window_get_scale_factor(window);
      gint ox = 0, oy = 0;
      gdk_window_get_origin(window, &ox, &oy);
      GtkAllocation alloc = {};
      gtk_widget_get_allocation(GTK_WIDGET(view), &alloc);
      if (!gtk_widget_get_has_window(GTK_WIDGET(view))) {
        ox += alloc.x * scale;
        oy += alloc.y * scale;
      }
      Display* xdisplay = GDK_DISPLAY_XDISPLAY(display);
      ::Window root = DefaultRootWindow(xdisplay);
      int rx = ox + static_cast<int>(lx * scale + 0.5);
      int ry = oy + static_cast<int>(ly * scale + 0.5);
      warp(xdisplay, None, root, 0, 0, 0, 0, rx, ry);
      if (XFlushFn flush = nmc_x_flush()) flush(xdisplay);
    }
  }
#endif
  (void)self;
  (void)args;
  return FL_METHOD_RESPONSE(fl_method_success_response_new(nullptr));
}

