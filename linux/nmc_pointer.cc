// ---- relative_motion listener (typed via the generated header) --------------
// Accumulate the UNACCELERATED dx/dy. GNOME/Mutter's pointer-acceleration curve
// flattens SLOW movement toward zero, so the accelerated `dx` is ~0 for a
// controlled scrub (→ "can't change the value"). The unaccelerated delta is
// linear and preserves slow movement, which is what a scrub needs. wl_fixed_t is
// 24.8 fixed point → /256 for the logical value.
static void nmc_wl_relative_motion(void* data,
                                   struct zwp_relative_pointer_v1* /*relptr*/,
                                   uint32_t /*utime_hi*/, uint32_t /*utime_lo*/,
                                   wl_fixed_t /*dx*/, wl_fixed_t /*dy*/,
                                   wl_fixed_t dx_unaccel,
                                   wl_fixed_t dy_unaccel) {
  auto* self = static_cast<NmcPointer*>(data);
  self->wl_rel_dx += static_cast<double>(dx_unaccel) / 256.0;
  self->wl_rel_dy += static_cast<double>(dy_unaccel) / 256.0;
}

static const struct zwp_relative_pointer_v1_listener nmc_wl_relptr_listener = {
    nmc_wl_relative_motion};

// Registry listener to bind exactly one global by interface name.
namespace {
struct WlBindTarget {
  const char* want;
  uint32_t want_version;
  const struct wl_interface* iface;
  struct wl_proxy* registry;
  struct wl_proxy* result;
};

void nmc_wl_registry_global(void* data, struct wl_proxy* /*registry*/,
                            uint32_t name, const char* interface,
                            uint32_t version) {
  auto* t = static_cast<WlBindTarget*>(data);
  if (t->result != nullptr || strcmp(interface, t->want) != 0) return;
  uint32_t v = version < t->want_version ? version : t->want_version;
  t->result = nmc_wl::registry_bind(t->registry, name, t->iface, v);
}
void nmc_wl_registry_global_remove(void*, struct wl_proxy*, uint32_t) {}

// Non-const so it passes to wl_proxy_add_listener's void(**)(void) parameter
// without stripping const. (The Wayland API only reads it.)
void (*kRegistryListener[])(void) = {
    reinterpret_cast<void (*)(void)>(nmc_wl_registry_global),
    reinterpret_cast<void (*)(void)>(nmc_wl_registry_global_remove)};
}  // namespace

static bool nmc_wl_available(NmcPointer* self) {
  GdkDisplay* display = gdk_display_get_default();
  if (display == nullptr || !GDK_IS_WAYLAND_DISPLAY(display)) return false;
  return nmc_wl::core_ok() && get_gdk_window(self) != nullptr;
}

static struct wl_proxy* nmc_wl_bind(struct wl_display* dpy, const char* name,
                                    uint32_t version,
                                    const struct wl_interface* iface) {
  struct wl_proxy* registry = nmc_wl::get_registry(dpy);
  if (registry == nullptr) return nullptr;
  WlBindTarget target = {name, version, iface, registry, nullptr};
  nmc_wl::add_listener()(registry, kRegistryListener, &target);
  nmc_wl::roundtrip()(dpy);
  nmc_wl::proxy_destroy()(registry);
  return target.result;
}

